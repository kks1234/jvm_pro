package math

import (
	"jvm-project/src/chap7/instructions/base"
	"jvm-project/src/chap7/rtda"
)

// int 左移
type ISHL struct {
	base.NoOperandsInstruction
}

// int 算术右移
type ISHR struct {
	base.NoOperandsInstruction
}

// int 逻辑右移
type IUSHR struct {
	base.NoOperandsInstruction
}

// long 左移
type LSHL struct {
	base.NoOperandsInstruction
}

// long 算术右移
type LSHR struct {
	base.NoOperandsInstruction
}

// long 逻辑右移
type LUSHR struct {
	base.NoOperandsInstruction
}

func (I *ISHL) Execute(frame *rtda.Frame) {
	stack := frame.OperandStack()
	v2 := stack.PopInt()
	v1 := stack.PopInt()
	s := uint32(v2) & 0x1f
	result := v1 << s
	stack.PushInt(result)
}

func (I *ISHR) Execute(frame *rtda.Frame) {
	stack := frame.OperandStack()
	v2 := stack.PopInt()
	v1 := stack.PopInt()
	s := uint32(v2) & 0x1f
	result := v1 >> s
	stack.PushInt(result)
}

func (I *IUSHR) Execute(frame *rtda.Frame) {
	stack := frame.OperandStack()
	v2 := stack.PopInt()
	v1 := stack.PopInt()
	s := uint32(v2) & 0x1f
	result := int32(uint32(v1) >> s)
	stack.PushInt(result)
}

